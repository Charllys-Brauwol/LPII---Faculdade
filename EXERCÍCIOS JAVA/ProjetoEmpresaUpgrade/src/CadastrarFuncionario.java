    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int row = jTableFuncionarios.getSelectedRow();
        String id = jTableFuncionarios.getValueAt(row, 0).toString();

        String nomeFuncionario = jTextNome.getText();
        String funcao = jComboBoxFuncao.getSelectedItem().toString();
        String rg = jTextRg.getText();
        String cpf = jTextCpf.getText();
        String telefone = jTextTelefone.getText();
        String rua = jTextRua.getText();
        String numero = jTextNumeroCasa.getText();
        String bairro = jTextBairro.getText();
        String cidade = jTextCidade.getText();

        if (!nomeFuncionario.isEmpty()) {
            try {
                Conexao con = new Conexao();
                java.sql.Statement st = con.conexao.createStatement();
                String sql = "UPDATE funcionarios SET nome='" + nomeFuncionario + "',"
                + "funcao='" + funcao + "',"
                + "rg='" + rg + "',"
                + "cpf='" + cpf + "',"
                + "telefone='" + telefone + "',"
                + "rua='" + rua + "',"
                + "numero='" + numero + "',"
                + "bairro='" + bairro + "',"
                + "cidade='" + cidade + "'"
                + " WHERE id=" + id;

                if (st.executeUpdate(sql) == 1) {
                    JOptionPane.showMessageDialog(null, "Dados alterados");
                    jButtonEditar.setEnabled(false);
                    jButtonExcluir.setEnabled(false);
                    jButtonSalvar1.setEnabled(true);
                    limparCampos();
                    popularTabelaFuncionarios();

                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao alterar!");
                }

            } catch (SQLException ex) {
                Logger.getLogger(Cidades.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ops!, Preencha os campos para continuar! ");
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

