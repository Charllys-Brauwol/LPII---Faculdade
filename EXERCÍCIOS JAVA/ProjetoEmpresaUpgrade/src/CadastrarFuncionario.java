public void popularTabelaFuncionarios() throws SQLException {
        String nomeBuscado = jTextPesquisar.getText();
        String tipoFuncionario = jComboBoxPesquisa.getSelectedItem().toString();

        DefaultTableModel model = (DefaultTableModel) jTableFuncionarios.getModel();
        model.setNumRows(0);
        Conexao con = new Conexao();
        java.sql.Statement st = con.conexao.createStatement();

        String sql = "SELECT * FROM funcionarios";

        if (!nomeBuscado.equals("") && !tipoFuncionario.equals("CARGO")) {
            sql += " WHERE nome LIKE '" + nomeBuscado + "%' AND funcao = '" + tipoFuncionario + "'";
        } if (!nomeBuscado.equals("")) {
            sql += " WHERE nome LIKE '" + nomeBuscado + "%'";
        } else if(!tipoFuncionario.equals("CARGO")) {
            sql += " WHERE funcao = '" + tipoFuncionario + "'";
        }
        
        System.out.println(sql);

        ResultSet resultado = st.executeQuery(sql);

        while (resultado.next()) {
            model.addRow(new Object[]{
                resultado.getString("id"),
                resultado.getString("nome"),
                resultado.getString("funcao"),
                resultado.getString("rg"),
                resultado.getString("cpf"),
                resultado.getString("telefone"),
                resultado.getString("rua"),
                resultado.getString("numero"),
                resultado.getString("bairro"),
                resultado.getString("cidade")
            });
        }
    }

