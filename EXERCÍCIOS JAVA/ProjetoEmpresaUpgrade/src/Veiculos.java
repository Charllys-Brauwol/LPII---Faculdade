public void popularTabelaVeiculos() throws SQLException {
        String veiculo = jTextFieldPesquisar.getText();

        DefaultTableModel model = (DefaultTableModel) jTableVeiculos.getModel();
        model.setNumRows(0);
        Conexao con = new Conexao();
        java.sql.Statement st = con.conexao.createStatement();

        String sql = "SELECT * FROM veiculos ";

        if (!veiculo.isEmpty()) {
            sql += "WHERE veiculo LIKE '" + veiculo + "%'";
        } 
        System.out.println(sql);

        ResultSet resultado = st.executeQuery(sql);

        while (resultado.next()) {
            model.addRow(new Object[]{
                resultado.getString("id"),
                resultado.getString("veiculo"),
                resultado.getString("placa"),
                resultado.getString("cor"),
                resultado.getString("ano")
            });
        }
    }

