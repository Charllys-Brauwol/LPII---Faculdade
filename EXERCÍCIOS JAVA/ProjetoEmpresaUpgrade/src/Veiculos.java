    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // DECLARA AS VARIAVAEIS
        String veiculo = jTextFieldVeiculo.getText();
        String placa = jTextFieldPlaca1.getText();
        String cor = jTextFieldCor.getText();
        String ano = jTextFieldAno.getText();
        
        //FAZ A VERIFICAÇÂO SE ESTA TUDO PREENCHIDO COM UM IF ELSE
        if(veiculo.equals("") || placa.equals("") || cor.equals("") || ano.equals("")){
            //MENSAGEM QUE APARECE PARA O USUÁRIO PREENCHER TUDO
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!!!");
        } else {
            try{
                //CRIA O OBJETO QUE FAZ A CONEXAO COM O BD
                Conexao con = new Conexao();
                Statement st = (Statement) con.conexao.createStatement();
                //STRING QUE EXECUTA A INSERCAO DOS DADOS NO BD
                String sql = "INSERT INTO veiculos (veiculo, placa, cor, ano) VALUES ('" + veiculo + "','" + placa +  "','" + cor +"','" + ano + "' )";

                //IF ELSE DE VERIFICACAO SE OS DADOS FORAM INSERIDOS
                if (st.execute(sql)) {
                    JOptionPane.showMessageDialog(null, "Ops! Algo errado");
                } else {
                    JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso!");
                    jTextFieldVeiculo.setText("");
                    jTextFieldPlaca1.setText("");
                    jTextFieldCor.setText("");
                    jTextFieldAno.setText("");
                    popularTabelaVeiculos();
                }
            } catch (Exception e) {
                System.out.println("erro");
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

