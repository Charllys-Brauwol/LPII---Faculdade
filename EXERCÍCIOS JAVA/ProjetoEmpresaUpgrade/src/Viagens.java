public void popularTabelaViagens() throws SQLException {

        DefaultTableModel model = (DefaultTableModel) jTableViagens.getModel();
        model.setNumRows(0);
        
        Conexao con = new Conexao();
        java.sql.Statement st = con.conexao.createStatement();

        String sql = "SELECT funcionarios.nome, veiculos.veiculo, viagens.* FROM `viagens` JOIN funcionarios ON funcionarios.id = viagens.funcionario JOIN veiculos ON veiculos.id = viagens.veiculo ORDER BY viagens.id DESC";
        
        System.out.println(sql);

        ResultSet resultado = st.executeQuery(sql); 
        
        while (resultado.next()) {
            String dataTabela = resultado.getString("data"); 
            Date dt;
            try {
                dt = new SimpleDateFormat("yyyy-MM-dd").parse(dataTabela);
                String dataFormatada = new SimpleDateFormat("dd-MM-yyyy").format(dt);
                model.addRow(new Object[]{
                resultado.getString("id"),
                resultado.getString("nome"),
                resultado.getString("veiculo"),
                resultado.getString("local_saida"), 
                resultado.getString("destino"),                 
                dataFormatada,
                resultado.getString("km_saida"),
                resultado.getString("km_chegada")
            });
            } catch (ParseException ex) {
                Logger.getLogger(Abastecimentos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }


